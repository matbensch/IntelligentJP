function startDrawing(e)
{
    isDrawing = true;
    draw(e);
}

function draw({offsetX: x,offsetY: y})
{
    if(!isDrawing) return;

    ctx.lineWidth = 9;//10;
    ctx.lineCap = "round";
    ctx.strokeStyle = "#ffffff";

    ctx.lineTo(x, y);
    ctx.stroke();
    ctx.beginPath();
    ctx.moveTo(x,y);
}

function endDrawing()
{
    isDrawing = false;
    submitImage();
    ctx.beginPath();
}

function clearCanvas()
{
    ctx.clearRect(0,0,canvas.width,canvas.height);
}

async function resizeImageData (imageData, width, height) {
    const resizeWidth = width;
    const resizeHeight = height;
    const ibm = await window.createImageBitmap(imageData, 0, 0, imageData.width, imageData.height, {
      resizeWidth, resizeHeight
    });
    const newCanvas = document.createElement('canvas')
    newCanvas.width = resizeWidth
    newCanvas.height = resizeHeight
    const newCtx = newCanvas.getContext('2d')
    newCtx.drawImage(ibm, 0, 0)
    return newCtx.getImageData(0, 0, resizeWidth, resizeHeight)
  }

