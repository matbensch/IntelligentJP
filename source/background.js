function objectToImageData(obj)
{
    var size = 0;
    var key;
    for(key in obj)
        {if(obj.hasOwnProperty(key))
        {size++;}}
            
    var clampedArray = new Uint8ClampedArray(size);
    
    for(var i=0;i<size;i++)
    {
        clampedArray[i] = obj[i];
    }
    var passData = new ImageData(clampedArray,64,64);
    return passData;
}


async function loadModel()
{
    if(model==null)
    {
        model = await tf.loadLayersModel("tmp/js-japanese-modelv4/model.json");
        model.summary();
    }
}

function center(inp_image_array)
{
    var x_sum = 0;
    var y_sum = 0;
    var m_sum = 0;

    for(var x=0;x<inp_image_array.length;x++)
    {
        for(var y=0;y<inp_image_array[0].length;y++)
        {
            if(inp_image_array[x][y]>0)
            {
                x_sum+=x;
                y_sum+=y;
                m_sum+=1;
            }
        }
    }

    var x_com = 0;
    var y_com = 0;
    
    if(m_sum!=0)
    {
        x_com = Math.floor(x_sum/m_sum);
        y_com = Math.floor(y_sum/m_sum);        
    }

    var x_correction = x_com - 32;
    var y_correction = y_com - 32;
    
    var ret_array = new Array(64);
    for(var i=0;i<64;i++){ret_array[i] = new Float32Array(64); ret_array[i].fill(0); } 

    for(var x=0;x<inp_image_array.length;x++)
    {
        for(var y=0;y<inp_image_array[0].length;y++)
        {
            if(inp_image_array[x][y]>0)
            {
                var new_x = x-x_correction;
                var new_y = y-y_correction;
                if(0<=new_x && new_x<=63 && 0<=new_y && new_y<=63) ret_array[new_x][new_y] = 1;
            }
        }
    }
    
    return ret_array;
}

async function predict(imgData)
{
    await loadModel();

    var tf_image = tf.browser.fromPixels(imgData);

    const rgb_weights = [0.2989,0.5870,0.1140];
    tf_image = tf.mul(tf_image,rgb_weights);
    tf_image = tf.sum(tf_image,axis=-1);
    
    var tf_image_array = await tf_image.array();
    tf_image_array = await center(tf_image_array);
    tf_image = tf.tensor(tf_image_array);
    
    tf_image = tf.expandDims(tf_image,axis=-1);
    tf_image = tf.expandDims(tf_image,axis=0);

    const pred = model.predict(tf_image);

    const {values,indices} = tf.topk(pred,5);

    const indices_array = await indices.data();
    var ret = new Array(indices_array.length);

    for(var i=0;i<indices_array.length;i++) ret[i] = label_to_character[indices_array[i]];

    chrome.runtime.sendMessage
    ({
        msgData:ret
    });
}

